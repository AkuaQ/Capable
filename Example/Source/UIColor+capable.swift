import UIKit

extension UIColor {
    static var capableGreen: UIColor {
        return UIColor(red: 137 / 255.0, green: 176 / 255.0, blue: 106 / 255.0, alpha: 1.0)
    }

    static var capableRed: UIColor {
        return UIColor(red: 1.0, green: 120 / 255.0, blue: 120 / 255.0, alpha: 1.0)
    }
}
